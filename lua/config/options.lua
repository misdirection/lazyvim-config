-- Sets clipboard when using WSL
if vim.fn.has("wsl") == 1 then
  vim.g.clipboard = {
    name = "WslClipboard",
    copy = {
      ["+"] = "clip.exe",
      ["*"] = "clip.exe",
    },
    paste = {
      ["+"] = 'powershell.exe -c Get-Clipboard',
      ["*"] = 'powershell.exe -c Get-Clipboard',
    },
    cache_enabled = 0,
  }
end
